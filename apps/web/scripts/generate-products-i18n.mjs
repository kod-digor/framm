function humanize(id) {
  if (TASK_LABELS[id]) return TASK_LABELS[id];
  return id
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

function phaseLabel(id) {
  if (PHASE_LABELS[id]) return PHASE_LABELS[id];
  return {
    title: id.charAt(0).toUpperCase() + id.slice(1),
    goal: "Développement de cette phase",
  };
}

const catalogSrc = readFileSync(join(ROOT, "src/lib/products-catalog.ts"), "utf8");
const keys = [
  "mail", "contacts", "chat", "newsletter", "drive", "photos", "office", "notes",
  "forms", "website", "calendar", "conference", "collab-tools", "passwords", "backups",
  "ai", "dns", "vpn", "proxy", "browser", "accounting", "members", "maps", "video",
  "opensource-support",
];

const products = { categories: {}, items: {} };

const CATEGORY_LABELS = {
  communication: "Communication",
  productivity: "Productivité",
  collaboration: "Collaboration",
  security: "Sécurité",
  infrastructure: "Infrastructure",
  association: "Association",
  media: "Médias",
  opensource: "Open source",
};

for (const [k, v] of Object.entries(CATEGORY_LABELS)) {
  products.categories[k] = v;
}

for (const key of keys) {
  const start = catalogSrc.indexOf(`key: "${key}"`);
  const nextKey = catalogSrc.indexOf('key: "', start + 10);
  const block = nextKey > start ? catalogSrc.slice(start, nextKey) : catalogSrc.slice(start);

  const phaseIds = [...block.matchAll(/phase\("([^"]+)"/g)].map((m) => m[1]);
  const taskIds = [...block.matchAll(/task\("([^"]+)"/g)].map((m) => m[1]);

  const meta = PRODUCT_META[key];
  const item = {
    title: meta.title,
    tagline: meta.tagline,
    description: meta.description,
    phases: {},
    tasks: {},
  };

  for (const pid of phaseIds) {
    const pl = phaseLabel(pid);
    item.phases[pid] = { title: pl.title, goal: pl.goal };
  }

  for (const tid of taskIds) {
    item.tasks[tid] = humanize(tid);
  }

  products.items[key] = item;
}

const outPath = join(ROOT, "messages/products.generated.json");
writeFileSync(outPath, JSON.stringify(products, null, 2) + "\n");
